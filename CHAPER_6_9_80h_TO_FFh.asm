;WRITE A PROGRAM TO DISPLAY THE EXTENDED ASCII CHARACTERS
;(ASCII CODES 80H TO FFH). DISPLAY 10 CHARACTERS PER LINE
;,SEPERATED BY BLANKS.STOP AFTER THE EXTENDED CHARACTERS
;HAVE BEEN DISPLAYED ONCE...

.MODEL SMALL
.STACK 100H
.CODE
MAIN PROC 
    MOV CX,80H
    MOV BL,1
    
    START:
    CMP CX,0FFH
    JA EXIT
    
    
    PRINT:
    MOV AH,2
    MOV DX,CX
    INT 21H 
    MOV DX,32
    INT 21H
    INC CX 
    INC BL
    CMP BL,10
    JA NEWLINE
    JMP START
    
    NEWLINE:
    MOV AH,2
    MOV DL,13
    INT 21H
    MOV DL,10
    INT 21H 
    MOV BL,1
    JMP START
    
   
    
    
    
    
    EXIT:
    MOV AH,4CH
    INT 21H
END